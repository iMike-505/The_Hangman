def read_random():
    words=[]
    with open("./archivos/random_words.txt", "r", encoding="utf-8") as f:
        for line in f:
            words.append(line.rstrip())
    return random.choice(words)

def saved_word():
    word=read_random()
    unknown_word= "-" * len(word)
    return word, unknown_word

def replace(word, unknown_word, letter):
    letter_numbers = word.count(letter)
    beginning = 0
    for i in range(letter_numbers):
        position = word.find(letter,beginning)
        unknown_word = unknown_word[:position] + letter + unknown_word[position+1:] 
        beginning = position + 1
    return unknown_word
